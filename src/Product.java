public class Product {
    int id, amount;
    String name, brand, type, size, color, description;
    double price;
    public Product(int id, int amount, String name, String brand, String type, String size, String color,
            String description, double price) {
        this.id = id;
        this.amount = amount;
        this.name = name;
        this.brand = brand;
        this.type = type;
        this.size = size;
        this.color = color;
        this.description = description;
        this.price = price;
    }




}
